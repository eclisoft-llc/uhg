CREATE TABLE IF NOT EXISTS COMPACT.BENEFIT_PROGRAM (
    BENEFIT_PROGRAM_ID NUMBER NOT NULL CONSTRAINT PK_BENEFIT_PROGRAM_ID PRIMARY KEY,
	PROGRAM_NAME VARCHAR(1000) NOT NULL,
	PROGRAM_DESC VARCHAR(1000) NOT NULL,
	START_DATE DATE,
	END_DATE DATE,
	HAS_EXPIRED NUMBER NOT NULL,
	IS_ACTIVE NUMBER,
	CREATED_ON DATETIME,
	DELETED_BY NUMBER,
	DELETED_ON DATETIME,
	CREATED_BY NUMBER,
	UPDATED_BY NUMBER,
	UPDATED_ON DATETIME,
	ADDITIONAL_DAYS NUMBER NOT NULL,
	IS_MTM NUMBER NOT NULL,
	IS_SENSITIVE NUMBER,
	DW_INSERT_DATETIME DATETIME,
	DW_UPDATE_DATETIME DATETIME,
    SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_PROCESS_ID VARCHAR(36)
);