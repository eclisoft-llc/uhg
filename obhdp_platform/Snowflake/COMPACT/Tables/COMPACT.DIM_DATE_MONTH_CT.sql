CREATE TABLE IF NOT EXISTS COMPACT.DIM_DATE_MONTH (
	MONTH_DIM_ID INTEGER NOT NULL CONSTRAINT PK_DIM_DATE_MONTH PRIMARY KEY,
	MONTH_DESC VARCHAR(10),
	MONTH_NBR INTEGER,
	QUARTER_DIM_ID INTEGER,
	QUARTER_DESC VARCHAR(8),
	CAL_YEAR_QUARTER INTEGER,
	YEAR_DIM_ID INTEGER NOT NULL,
	YEAR_NBR INTEGER,
	DW_INSERT_DATETIME DATETIME,
	DW_UPDATE_DATETIME DATETIME,
	MONTH_BEGIN_DATE DATE,
	MONTH_END_DATE DATE,
	QUARTER_BEGIN_DATE DATE,
	QUARTER_END_DATE DATE,
	YEAR_BEGIN_DATE DATE,
	YEAR_END_DATE DATE,
	CAL_YEAR_MONTH INTEGER,
	LMO_MONTH_DIM_ID INTEGER,
	LYR_LMO_MONTH_DIM_ID INTEGER,
	LYR_MONTH_DIM_ID INTEGER,
	MONTH_NAME VARCHAR(3),
	CURR_MONTH_IND INTEGER,
	SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_PROCESS_ID VARCHAR(36),
	CONSTRAINT FK_DIM_DT_MNTH_QUARTER_DIM_ID FOREIGN KEY (QUARTER_DIM_ID) REFERENCES COMPACT.DIM_DATE_QUARTER(QUARTER_DIM_ID),
	CONSTRAINT FK_DIM_DT_MNTH_YEAR_DIM_ID FOREIGN KEY (YEAR_DIM_ID) REFERENCES COMPACT.DIM_DATE_YEAR(YEAR_DIM_ID)
);

COMMENT ON TABLE COMPACT.DIM_DATE_MONTH IS 'This table stores attributes that describe multiple ways for selecting month dates for reporting.';
