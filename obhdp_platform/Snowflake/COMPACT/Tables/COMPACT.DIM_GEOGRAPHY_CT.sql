CREATE TABLE IF NOT EXISTS COMPACT.DIM_GEOGRAPHY (
ZIP_CODE VARCHAR(5) NOT NULL CONSTRAINT UK_F_DIM_GEOGRAPHY_ZIP_CODE UNIQUE,
AREA VARCHAR(12),
COUNTY VARCHAR(5),
STATE VARCHAR(6) NOT NULL,
DW_INSERT_DATETIME DATETIME,
DW_UPDATE_DATETIME DATETIME,
GEOGRAPHY_DIM_ID INTEGER NOT NULL CONSTRAINT PK_F_DIM_GEOGRAPHY_DIM_ID PRIMARY KEY,
GEOGRAPHY_STATE_DIM_ID INTEGER NOT NULL,
STATE_DESC VARCHAR(20),
GEOGRAPHY_COUNTY_DIM_ID INTEGER NOT NULL,
COUNTY_DESC VARCHAR(25),
CITY_DESC VARCHAR(28) NOT NULL,
AREA_DESC VARCHAR(25),
GEOGRAPHY_REGION_DIM_ID INTEGER NOT NULL DEFAULT -1,
REGION VARCHAR(12),
REGION_DESC VARCHAR(25),
GEOGRAPHY_AREA_DIM_ID INTEGER NOT NULL DEFAULT -1,
ZIP_LATITUDE NUMBER(38,10),
ZIP_LONGITUDE NUMBER(38,10),
SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
SF_PROCESS_ID VARCHAR(36)
);
