CREATE TABLE IF NOT EXISTS COMPACT.DIM_IDENT_TYPES (
IDENT_TYPE_DIM_ID NUMBER NOT NULL CONSTRAINT PK_F_DIM_IDENT_TYPES_DIM_ID PRIMARY KEY,
IDENT_TYPE_CODE VARCHAR(10) NOT NULL,
IDENT_TYPE_DESC VARCHAR(100) NOT NULL,
DW_INSERT_DATETIME DATE,
DW_UPDATE_DATETIME DATE,
SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
SF_PROCESS_ID VARCHAR(36),
CONSTRAINT UK_F_DIM_IDENT_TYPES_TYPE_CODE UNIQUE (IDENT_TYPE_CODE)
);
