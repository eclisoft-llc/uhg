
CREATE TABLE IF NOT EXISTS
   COMPACT.DIM_PROGRAM_STATUS_CODE
    (
        PROGRAM_STATUS_DIM_ID INTEGER NOT NULL CONSTRAINT PK_F_DIM_PROGRAM_STATUS_DIM_ID PRIMARY KEY,
        PROGRAM_STATUS_CODE VARCHAR(5),
        PROGRAM_STATUS_CODE_DESC VARCHAR(100),
        DW_INSERT_DATE DATETIME,
        DW_UPDATE_DATE DATETIME,
	SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
	SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
	SF_PROCESS_ID VARCHAR(36)
    );
COMMENT ON TABLE COMPACT.DIM_PROGRAM_STATUS_CODE
IS
    'This table stores attributes that hold status codes for a program.';
COMMENT ON COLUMN COMPACT.DIM_PROGRAM_STATUS_CODE.PROGRAM_STATUS_DIM_ID
IS
    'This is the unique identifier generated by the Data Warehouse';
COMMENT ON COLUMN COMPACT.DIM_PROGRAM_STATUS_CODE.DW_INSERT_DATE
IS
    'Date the record was added to the Data Warehouse.';
COMMENT ON COLUMN COMPACT.DIM_PROGRAM_STATUS_CODE.DW_UPDATE_DATE
IS
    'Date the record was updated in the Data Warehouse.';

COMMENT ON COLUMN  COMPACT.DIM_PROGRAM_STATUS_CODE.SF_INSERT_DATETIME
IS
    'Date the record was added to the OBHDP.';
COMMENT ON COLUMN  COMPACT.DIM_PROGRAM_STATUS_CODE.SF_UPDATE_DATETIME
IS
    'Date the record was updated in the OBHDP.';

COMMENT ON COLUMN  COMPACT.DIM_PROGRAM_STATUS_CODE.SF_PROCESS_ID
IS
    'Unique identifier assigned to the process that inserts/updates the data in OBHDP.';

