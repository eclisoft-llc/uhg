CREATE TABLE IF NOT EXISTS COMPACT.DIM_RATECELL (
	RTCL_DIM_ID INTEGER NOT NULL CONSTRAINT PK_F_DIM_RATECELL_DIM_ID PRIMARY KEY,
	RTCL_CODE VARCHAR(30) NOT NULL,
	RTCL_DESC VARCHAR(200),
	COMPANY_DIM_ID INTEGER NOT NULL,
	RTCL_STATE VARCHAR(2) NOT NULL,
	RTCL_GROUP VARCHAR(200),
	RTCL_REGION_DIM_ID INTEGER NOT NULL DEFAULT -1 ,
	RTCL_CCOA VARCHAR(36),
	RTCL_COA VARCHAR(45),
	RTCL_GENDER_DIM_ID INTEGER NOT NULL DEFAULT -1 ,
	RTCL_AGE_BEGIN INTEGER,
	RTCL_AGE_END INTEGER,
	RTCL_AGE_TYPE VARCHAR(1),
	DW_INSERT_DATETIME DATETIME,
	DW_UPDATE_DATETIME DATETIME,
	RTCL_CATEGORY_DIM_ID INTEGER NOT NULL DEFAULT -1,
	EFFECTIVE_DATE DATE,
	TERM_DATE DATE,
	COMMENTS VARCHAR(100),
	CLIENT_CODE VARCHAR(5),
	SEGMENT_ID VARCHAR(3),
	CONTRACT_CODE VARCHAR(15),
	SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
	SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
	SF_PROCESS_ID VARCHAR(36),
	CONSTRAINT UK_F_DIM_RATECELL_CODE_COMPANY_SEGMENT UNIQUE (RTCL_CODE,COMPANY_DIM_ID,SEGMENT_ID),
	CONSTRAINT FK_DIM_RATECELL_RTCL_CATEGORY_DIM_ID FOREIGN KEY (RTCL_CATEGORY_DIM_ID) REFERENCES COMPACT.DIM_RATECELL_CATEGORY(RTCL_CATEGORY_DIM_ID)
);
