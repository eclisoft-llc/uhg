CREATE TABLE IF NOT EXISTS COMPACT.INDEX_NAME (
    INDEX_ID NUMBER NOT NULL CONSTRAINT PK_INDEX_NAME_ID PRIMARY KEY,
	INDEX_NAME VARCHAR(100)  NOT NULL,
	INDEX_TYPE CHAR(1)  NOT NULL,
	SOURCE_TYPE CHAR(1)  NOT NULL,
	SOURCE_ID VARCHAR(50) NOT NULL,
	SOURCE_NAME VARCHAR(100),
	NOTES VARCHAR(200),
	INDEX_DESCRIPTION VARCHAR(50),
	IS_ACTIVE NUMBER,
	REPORT_INDEX_ID NUMBER,
	IS_DEFAULT NUMBER,
	IS_MEMBER_MERGE_CONFIGURED VARCHAR(1),
	INDEX_ORDER_ID NUMBER,
	CRM_IS_ACTIVE VARCHAR(1),
	CREATED_BY NUMBER,
	CREATED_ON DATETIME,
	UPDATED_BY NUMBER,
	UPDATED_ON DATETIME,
	DELETED_BY NUMBER,
	DELETED_ON DATETIME,
	DW_INSERT_DATETIME DATETIME,
	DW_UPDATE_DATETIME DATETIME,
    SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_PROCESS_ID VARCHAR(36)
    );