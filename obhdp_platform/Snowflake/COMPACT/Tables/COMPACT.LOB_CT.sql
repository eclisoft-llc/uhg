CREATE TABLE IF NOT EXISTS COMPACT.LOB (
	LOB_ID NUMBER NOT NULL CONSTRAINT PK_LOB_ID PRIMARY KEY,
	LOB_NAME VARCHAR(1000)  NOT NULL,
	LOB_DESC VARCHAR(1000) NOT NULL,
	IS_ACTIVE NUMBER,
	CREATED_ON DATETIME,
	DELETED_BY NUMBER,
	DELETED_ON DATETIME,
	CREATED_BY NUMBER,
	UPDATED_BY NUMBER,
	UPDATED_ON DATETIME,
	START_DATE DATE,
	END_DATE DATE,
	GROUP_ID VARCHAR(50),
	GROUP_DESC VARCHAR(500),
	CRM_IS_ACTIVE VARCHAR(1),
	RANK_NO NUMBER,
	DW_INSERT_DATETIME DATETIME,
	DW_UPDATE_DATETIME DATETIME,
    SF_INSERT_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_UPDATE_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
    SF_PROCESS_ID VARCHAR(36)
	);