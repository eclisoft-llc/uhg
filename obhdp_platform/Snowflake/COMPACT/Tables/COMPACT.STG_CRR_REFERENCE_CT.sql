   CDC_FLAG VARCHAR(2),
   CDC_TS TIMESTAMP_NTZ(9),
   CHANGEDATETIME TIMESTAMP_NTZ(9),
   CHANGEUSERID VARCHAR(20),
   CREATEDATETIME TIMESTAMP_NTZ(9),
   CREATEUSERID VARCHAR(20),
   ENDDATE DATE,
