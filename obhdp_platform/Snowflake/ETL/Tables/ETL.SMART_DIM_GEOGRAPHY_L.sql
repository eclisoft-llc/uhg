ZIP_CODE VARCHAR(5) NOT NULL CONSTRAINT UK_DIM_GEOGRAPHY_ZIP_CODE UNIQUE,
AREA VARCHAR(12),
COUNTY VARCHAR(5),
STATE VARCHAR(6) NOT NULL,
DW_INSERT_DATETIME VARCHAR(35),
DW_UPDATE_DATETIME VARCHAR(35),
GEOGRAPHY_DIM_ID VARCHAR(50) NOT NULL CONSTRAINT PK_DIM_GEOGRAPHY_DIM_ID PRIMARY KEY,
