create or replace view FOUNDATION.IMDM_INDV_EDWPERSID_DEDUP(
	EDWPRSID,
	SMRTPRSID,
	PERS_COUNT
) as
SELECT DISTINCT
FIRST_VALUE(EDWPRSID) OVER(PARTITION BY SMRTPRSID ORDER BY EDWPRSID) EDWPRSID,
SMRTPRSID,
COUNT(EDWPRSID) OVER(PARTITION BY SMRTPRSID) PERS_COUNT
FROM
(SELECT DISTINCT
SMRTPRSID,
EDWPRSID
FROM COMPACT.IMDM_INDV);