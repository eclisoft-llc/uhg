   GENDER,
   BIRTH_DATE,
   DECEASED,
   SOURCE_SYSTEM_CODE,
   EPIM_LAST_UPDATED_DTTM,
   AUDIT_CREATE_DATE
FROM ETL.EPIM_PROVIDER_NAME_L;
